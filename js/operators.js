// operadores basicos
// operadores aritmeticos
// +, - , * , / , % , **

const num1 = 10
const num2 = 5

console.log('10 + 5');
let suma = num1 + num2
console.log(suma);

console.log('10 - 5');
let resta = num1 - num2
console.log(resta);

console.log('10 x 5');
let multiplicacion = num1 * num2
console.log(multiplicacion);

console.log('10 / 5');
let division = num1 / num2
console.log(division);

console.log('resto de 10 / 5');
let resto = num1 % num2 // el resto de una division
console.log(resto);

console.log('10 elevado a la 5');
let exponenciacion = num1 ** num2 // despues del ** va a cuanto va elevado
console.log(exponenciacion);

